/// Export rules as a standalone shell script.
///
/// The script uses `iptables-restore --noflush` with the same format as
/// the apply engine, making it safe to run on a host that already has
/// system-managed chains.
///
/// Each rule is paired with a chain name via `(chain, rule)` tuples.
pub fn export_shell_script(rules: &[(&str, &RuleSpec)], table: &str) -> String {
    let mut out = String::new();

    out.push_str("#!/bin/bash\n");
    out.push_str("# Traffic Rules — exported iptables rules\n");
    out.push_str("# Generated by Traffic Rules app\n");
    out.push_str("set -euo pipefail\n\n");

    // Generate iptables-restore content
    let restore_content = build_restore_content(rules, table);

    out.push_str("# Apply rules via iptables-restore (atomic)\n");
    out.push_str("cat <<'IPTABLES_RULES' | sudo iptables-restore -w 5 --noflush --counters\n");
    out.push_str(&restore_content);
    out.push_str("IPTABLES_RULES\n\n");
    out.push_str("echo \"Rules applied successfully.\"\n");

    out
}

/// Export rules as an Ansible playbook.
///
/// Each rule is paired with a chain name via `(chain, rule)` tuples.
pub fn export_ansible_playbook(rules: &[(&str, &RuleSpec)], hostname: &str) -> String {
    let mut out = String::new();

    out.push_str("---\n");
    out.push_str("# Traffic Rules — exported Ansible playbook\n");
    out.push_str("# Generated by Traffic Rules app\n");
    out.push_str("- name: Apply Traffic Rules iptables configuration\n");
    out.push_str(&format!("  hosts: {}\n", hostname));
    out.push_str("  become: true\n");
    out.push_str("  tasks:\n");

    for (i, (chain, rule)) in rules.iter().enumerate() {
        let args = rule_spec_to_args(rule);

        let target = rule
            .target
            .as_ref()
            .map(|t| t.to_string())
            .unwrap_or_else(|| "ACCEPT".to_string());

        let comment = rule
            .comment
            .as_ref()
            .map(|c| c.to_string())
            .unwrap_or_else(|| format!("Rule {}", i + 1));

        // Escape embedded quotes and wrap in double quotes to prevent YAML injection
        let safe_comment = comment.replace('\\', "\\\\").replace('"', "\\\"");
        out.push_str(&format!("    - name: \"{}\"\n", safe_comment));
        out.push_str("      ansible.builtin.iptables:\n");
        out.push_str(&format!("        chain: {}\n", chain));
        out.push_str(&format!("        jump: {}\n", target));

        if let Some(ref proto) = rule.protocol {
            out.push_str(&format!("        protocol: {}\n", proto));
        }

        if let Some(ref src) = rule.source {
            out.push_str(&format!("        source: {}\n", src.addr));
        }

        if let Some(ref dst) = rule.destination {
            out.push_str(&format!("        destination: {}\n", dst.addr));
        }

        if let Some(ref dp) = rule.dest_port {
            out.push_str(&format!(
                "        destination_port: \"{}\"\n",
                port_spec_to_string(dp)
            ));
        }

        if let Some(ref iface) = rule.in_iface {
            out.push_str(&format!("        in_interface: {}\n", iface.name));
        }

        if let Some(ref iface) = rule.out_iface {
            out.push_str(&format!("        out_interface: {}\n", iface.name));
        }

        if let Some(ref comment_text) = rule.comment {
            let safe = comment_text.replace('\\', "\\\\").replace('"', "\\\"");
            out.push_str(&format!("        comment: \"{}\"\n", safe));
        }

        let _ = args; // args used indirectly above
    }

    out
}

/// Export rules in `iptables-save` format.
///
/// Produces a file that can be applied with `iptables-restore --noflush`.
///
/// Each rule is paired with a chain name via `(chain, rule)` tuples.
pub fn export_iptables_save(rules: &[(&str, &RuleSpec)], table: &str) -> String {
    let mut out = String::new();
    out.push_str("# Generated by Traffic Rules app\n");
    out.push_str(&build_restore_content(rules, table));
    out
}

// ---------------------------------------------------------------------------
// Internal helpers
// ---------------------------------------------------------------------------

/// Build iptables-restore content with chain reset lines and COMMIT.
fn build_restore_content(rules: &[(&str, &RuleSpec)], table: &str) -> String {
    let mut out = String::new();

    // Collect chain names from rules
    let mut chain_names: std::collections::BTreeSet<String> =
        std::collections::BTreeSet::new();
    for (chain, _) in rules {
        chain_names.insert(chain.to_string());
    }
    // Ensure at least TR-INPUT exists when there are no rules
    if chain_names.is_empty() {
        chain_names.insert("TR-INPUT".to_string());
    }

    out.push_str(&format!("*{}\n", table));

    // Emit chain reset lines
    for chain in &chain_names {
        out.push_str(&format!(":{} - [0:0]\n", chain));
    }

    // Emit rules
    for (chain, rule) in rules {
        let args = rule_spec_to_args(rule);
        out.push_str(&format!("-A {} {}\n", chain, args.join(" ")));
    }

    out.push_str("COMMIT\n");
    out
}

