#[cfg(test)]
mod tests {
    use super::*;
    use crate::iptables::types::*;

    fn make_spec() -> RuleSpec {
        RuleSpec {
            protocol: None,
            protocol_negated: false,
            source: None,
            destination: None,
            in_iface: None,
            out_iface: None,
            matches: vec![],
            target: None,
            target_args: vec![],
            comment: None,
            counters: None,
            fragment: None,
            source_port: None,
            dest_port: None,
            address_family: AddressFamily::V4,
        }
    }

    #[test]
    fn test_export_shell_script() {
        let rule = RuleSpec {
            protocol: Some(Protocol::Tcp),
            dest_port: Some(PortSpec::Single(22)),
            target: Some(Target::Accept),
            source: Some(AddressSpec {
                addr: "10.0.0.0/8".to_string(),
                negated: false,
            }),
            ..make_spec()
        };
        let rules: Vec<(&str, &RuleSpec)> = vec![("TR-INPUT", &rule)];

        let script = export_shell_script(&rules, "filter");
        assert!(script.starts_with("#!/bin/bash\n"));
        assert!(script.contains("iptables-restore"));
        assert!(script.contains("*filter"));
        assert!(script.contains(":TR-INPUT"));
        assert!(script.contains("-A TR-INPUT"));
        assert!(script.contains("COMMIT"));
        assert!(script.contains("-p tcp"));
        assert!(script.contains("--dport 22"));
        assert!(script.contains("-j ACCEPT"));
    }

    #[test]
    fn test_export_shell_script_custom_chain() {
        let rule = RuleSpec {
            protocol: Some(Protocol::Tcp),
            dest_port: Some(PortSpec::Single(80)),
            target: Some(Target::Accept),
            ..make_spec()
        };
        let rules: Vec<(&str, &RuleSpec)> = vec![("TR-WEB", &rule)];

        let script = export_shell_script(&rules, "filter");
        assert!(script.contains(":TR-WEB"));
        assert!(script.contains("-A TR-WEB"));
    }

    #[test]
    fn test_export_ansible_playbook() {
        let rule = RuleSpec {
            protocol: Some(Protocol::Tcp),
            dest_port: Some(PortSpec::Single(80)),
            target: Some(Target::Accept),
            comment: Some("Allow HTTP".to_string()),
            ..make_spec()
        };
        let rules: Vec<(&str, &RuleSpec)> = vec![("TR-INPUT", &rule)];

        let playbook = export_ansible_playbook(&rules, "webservers");
        assert!(playbook.contains("hosts: webservers"));
        assert!(playbook.contains("become: true"));
        assert!(playbook.contains("ansible.builtin.iptables"));
        assert!(playbook.contains("chain: TR-INPUT"));
        assert!(playbook.contains("jump: ACCEPT"));
        assert!(playbook.contains("protocol: tcp"));
        assert!(playbook.contains("destination_port: \"80\""));
        assert!(playbook.contains("Allow HTTP"));
    }

    #[test]
    fn test_export_ansible_yaml_injection() {
        let rule = RuleSpec {
            protocol: Some(Protocol::Tcp),
            dest_port: Some(PortSpec::Single(80)),
            target: Some(Target::Accept),
            comment: Some("test\": malicious\nkey: value".to_string()),
            ..make_spec()
        };
        let rules: Vec<(&str, &RuleSpec)> = vec![("TR-INPUT", &rule)];

        let playbook = export_ansible_playbook(&rules, "webservers");
        // The comment should be safely escaped inside double quotes
        assert!(playbook.contains("- name: \"test\\\""));
        assert!(playbook.contains("comment: \"test\\\""));
    }

    #[test]
    fn test_export_iptables_save() {
        let rule = RuleSpec {
            protocol: Some(Protocol::Tcp),
            dest_port: Some(PortSpec::Multi(vec![80, 443])),
            target: Some(Target::Accept),
            ..make_spec()
        };
        let rules: Vec<(&str, &RuleSpec)> = vec![("TR-INPUT", &rule)];

        let save = export_iptables_save(&rules, "filter");
        assert!(save.contains("*filter"));
        assert!(save.contains(":TR-INPUT - [0:0]"));
        assert!(save.contains("-A TR-INPUT"));
        assert!(save.contains("COMMIT"));
    }

    #[test]
    fn test_export_shell_script_empty_rules() {
        let rules: Vec<(&str, &RuleSpec)> = vec![];
        let script = export_shell_script(&rules, "filter");
        assert!(script.contains("*filter"));
        assert!(script.contains("COMMIT"));
        assert!(script.contains(":TR-INPUT"));
    }
}
